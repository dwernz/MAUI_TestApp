        public string GetMonthString(int month)
        {
            string monthString = "";

            switch (month)
            {
                case 1:
                    monthString = "January";
                    break;
                case 2:
                    monthString = "February";
                    break;
                case 3: 
                    monthString = "March";
                    break;
                case 4: 
                    monthString = "April"; 
                    break;
                case 5: 
                    monthString = "May"; 
                    break;
                case 6:
                    monthString = "June";
                    break;
                case 7:
                    monthString = "July";
                    break;
                case 8:
                    monthString = "August";
                    break;
                case 9:
                    monthString = "September";
                    break;
                case 10:
                    monthString = "October";
                    break;
                case 11:
                    monthString = "November";
                    break;
                case 12:
                    monthString = "December";
                    break;
                default:
                    monthString = "Error";
                    break;
            }

            return monthString;
        }

        public string GetDayEnding(int day)
        {
            string dayEnding = "";

            if (day == 1 || day == 11 || day == 21 || day == 31)
            {
                dayEnding = "st";
            }
            else if (day == 2 || day == 22)
            {
                dayEnding = "nd";
            }
            else if (day == 3 || day == 23)
            {
                dayEnding = "rd";
            }
            else
            {
                dayEnding = "th";
            }

            return dayEnding;
        }
